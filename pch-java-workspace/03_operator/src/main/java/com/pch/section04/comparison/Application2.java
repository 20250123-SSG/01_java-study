package com.pch.section04.comparison;

public class Application2 {
    public static void main(String[] args) {
        char ch1 = 'a';
        char ch2 = 'A';
        System.out.println("ch1 == ch2 : " + (ch1 == ch2));
        System.out.println("ch1 != ch2 : " + (ch1 != ch2));
        System.out.println("ch1 > ch2 : " + (ch1 > ch2));
        System.out.println("ch1 < ch2 : " + (ch1 < ch2));

        String str1 = "java";
        String str2 = "java";
        System.out.println("str1 == str2 : " + (str1 == str2));
        System.out.println("str1 != str2 : " + (str1 != str2));
    }
}
