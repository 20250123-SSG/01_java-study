package com.jun.practice;

public class PracApplication1 {
    public static void main(String[] args) {
        int i1 = 20;
        int i2 = 30;
        System.out.println("더하기 결과 : " + (i1 + i2)); // 더하기 결과
        System.out.println("빼기 결과 : " + (i1 - i2)); // 빼기
        System.out.println("곱하기 결과 : " + i1 * i2); // 곱하기
        System.out.println("나누기 결과 : " + i1 / i2); // 나누기
        System.out.println("나머지 결과 : " + i1 % i2); // 나머지
    }
}
