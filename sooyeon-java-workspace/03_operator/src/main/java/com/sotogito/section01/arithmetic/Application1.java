package com.sotogito.section01.arithmetic;

public class Application1 {
    public static void main(String[] args) {
        /**
         * ## 산술연산자
         * 1. 이항 연산자
         * 2. 종류 : + - * / %
         * 3. 우선순위 : (*,%) -> (+,-)
         */

        int num1 = 10;
        int num2 = 3;

        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1 / num2);
        System.out.println(num1 % num2);
    }


}
