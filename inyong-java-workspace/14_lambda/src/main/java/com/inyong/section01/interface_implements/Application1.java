package com.inyong.section01.interface_implements;

public class Application1 {
    public static void main(String[] args) {
        /*
        인터페이스 구현 방식 1
        구현 목적에 맞게 해당 인터페이스를 구현한(implements) 각 클래스를 새로이 만들어 활용

        public interface A{
        // 추상메소드
        }

        public interface B implements A{
        // B 목적에 맞는 방식으로 오버라이딩 => 메소드 기능 완성
        }
         */


        // 단점 : 목적에 맞는 구현 클래스를 매번 만들어야되는게 번거로움
        // 장점 : 한번 구현된 클래스가 있기 때문에 필요하면 바로 생성해서 사용 가능
    }

}
