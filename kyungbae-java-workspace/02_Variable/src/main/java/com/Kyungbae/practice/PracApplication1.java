package com.Kyungbae.practice;

public class PracApplication1 { // class start

    public static void main(String[] args) { // static main start

        int i1 = 20;
        int i2 = 30;

        System.out.println("더하기 결과 : " + (i1 + i2));
        System.out.println("빼기 결과 : " + (i1 - i2));
        System.out.println("곱하기 결과 : " + (i1 * i2));
        System.out.println("나누기 결과 : " + (i1 / i2));
        System.out.println("나머지 결과 : " + (i1 % i2)) ;

    } // static main end

} // class start
